package com.abctech.projectcore.Loop;

public class WhileNumberPrint {
    /**
     * 1. Write a program to print numbers from 1 to 10 both using for and while loop.
     */
    public static void main(String[] args) {
        int i =1;
        while (i<=10){
            System.out.println(i);
            i++;
        }
    }
}
