package com.abctech.projectcore.Loop;

public class WhileNaturalNumber {
    /**
     * 2. Write a program to calculate the sum of the first 10 natural numbers both using for loop and while loop.
     */
    public static void main(String[] args) {
        int i=1;
        while (i<=10){
            System.out.println(i);
        i++;
        }

    }
}
