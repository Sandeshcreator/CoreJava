package com.abctech.projectcore.loop;

public class DoubleMultiplication {
    public static void main(String[] args) {
        for (int i = 1; i <=10 ; i++) {
            for (int j = 1; j <=5 ; j++) {
                System.out.println(i + "X" + j +" = "+(i*j));
            }

            System.out.println("");

        }
    }
}
